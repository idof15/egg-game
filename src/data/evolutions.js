export function getEvolutionFor(animalId) {
  return EVOLUTIONS.find(e => e.from === animalId);
}

export function canEvolve(animalId, count) {
  const evo = getEvolutionFor(animalId);
  if (!evo) return false;
  return count >= evo.requiredCount;
}
